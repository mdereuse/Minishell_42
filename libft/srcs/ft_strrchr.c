#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	i++;
	while (i > 0)
	{
		if (s[i - 1] == (char)c)
			return ((char*)(s + i - 1));
		i--;
	}
	return ((void*)0);
}
